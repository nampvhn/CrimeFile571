    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        int id = 0;
        String name = "";
        String desc = "";

        CrimeTypesDB r = new CrimeTypesDB();
        try {
            id = Integer.parseInt(txtID.getText().trim());
            name = txtName.getText().trim();
            desc = txtDesc.getText().trim();

            if (null == name || "".equals(name)) {
                JOptionPane.showMessageDialog(this, "You must input name.", "Error", JOptionPane.ERROR_MESSAGE);
                txtName.requestFocus();
                return;
            }

            if (r.update(id, name, desc)) {
                JOptionPane.showMessageDialog(this, "Updated successful.", "Message", JOptionPane.INFORMATION_MESSAGE);
                PnlCrimeType pnl = new PnlCrimeType();
                this.dispose();
                pnl.setVisible(true);

                pnl.tblModel.fireTableDataChanged();
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
            return;
        }
    }//GEN-LAST:event_btnSaveActionPerformed

