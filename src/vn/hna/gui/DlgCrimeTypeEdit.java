    public void loadData(Vector<CrimeTypes> v) {
        try {
            if (v.isEmpty()) {
                JOptionPane.showMessageDialog(this, "No data.", "Information", JOptionPane.INFORMATION_MESSAGE);
                return;
            }
            if (idx > v.size() - 1) {
                JOptionPane.showMessageDialog(this, "Out of index.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }
            CrimeTypes role = v.get(idx);
            txtID.setText(new Integer(role.getCrimeTypeID()).toString());
            txtName.setText(role.getCrimeTypeName());
            txtDesc.setText(role.getDescription());
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
    public Vector loadDataFromSQLServer() {
        CrimeTypesDB r = new CrimeTypesDB();
        try {
            return (Vector) r.viewCrimeTypes();
        } catch (Exception e) {
            e.printStackTrace();
            return null;
        }
    }
    
    private void setEnableOrDisableButton() {
        try {
            if (v == null || v.size() < 2) {
                btFirst.setEnabled(false);
                btPrevious.setEnabled(false);
                btNext.setEnabled(false);
                btLast.setEnabled(false);
            }
            else {
                if (idx == 0) {
                    btFirst.setEnabled(false);
                    btPrevious.setEnabled(false);
                    btNext.setEnabled(true);
                    btLast.setEnabled(true);
                } else if (idx == v.size() - 1) {
                    btFirst.setEnabled(true);
                    btPrevious.setEnabled(true);
                    btNext.setEnabled(false);
                    btLast.setEnabled(false);
                } else {
                    btFirst.setEnabled(true);
                    btPrevious.setEnabled(true);
                    btNext.setEnabled(true);
                    btLast.setEnabled(true);
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
