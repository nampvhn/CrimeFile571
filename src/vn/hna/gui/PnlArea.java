    public Vector loadColumnHeader() {
        vColumnHeader = new Vector();
        try {
            vColumnHeader.add("ID");
            vColumnHeader.add("Area Name");
        } catch (Exception e) {
            e.printStackTrace();
            return null;
        }
        return vColumnHeader;
    }

    public void loadTable() {
        AreasDB ct = new AreasDB();
        try {
            tblModel = (DefaultTableModel) tblCrimeType.getModel();
            vAll = (Vector<Areas>) ct.viewAreas();
            if (vAll.isEmpty()) {
                JOptionPane.showMessageDialog(this, "No data.", "Information", JOptionPane.INFORMATION_MESSAGE);
            }
            loadData(vAll);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    public List loadData(List<Areas> listAll) {
        try {
            if (listAll.isEmpty()) {
                JOptionPane.showMessageDialog(this, "No data.", "Information", JOptionPane.INFORMATION_MESSAGE);
            }

            vData = new Vector();
            for (Areas area : listAll) {
                Vector vRow = new Vector();

                vRow.add(area.getAreaID());
                vRow.add(area.getAreaName());

                vData.add(vRow);
            }
        } catch (Exception e) {
            e.printStackTrace();
            return null;
        }
        return vData;
    }

    private void sortColumn() {
        try {
            tblCrimeType.getTableHeader().setToolTipText("Click here to sort.");
            tblCrimeType.setAutoCreateRowSorter(true);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    public Vector searchByAreaName(Vector<Areas> v, String byName) {
        Vector<Areas> vFilter = new Vector();
        try {
            for (Areas area : v) {
                if (area.getAreaName().toUpperCase().contains(byName.toUpperCase())) {
                    vFilter.add(area);
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
            return null;
        }
        return vFilter;
    }

